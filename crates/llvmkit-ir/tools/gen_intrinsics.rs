const ROOT_TD: &str = "llvm/IR/Intrinsics.td";
const GENERATED_HEADER: &str = "// This file is @generated by tools/gen_intrinsics.rs from LLVM 22.1.4 TableGen.\n// Do not edit by hand.\n\n";
const VENDORED_LLVM_ROOT: &str = "tablegen/llvm-22.1.4";
const GENERATED_INTRINSICS: &str = "intrinsics_generated.rs";

// LLVM 22.1.4 exposes `exnref` as a WebAssembly primitive `ValueType`, but
// its upstream IIT enum stops at `IIT_V4096 = 53`. Keep the llvmkit-only
// encoding immediately after the upstream range instead of remapping exception
// references to `externref` / `funcref`.
const CUSTOM_IIT_WASM_EXNREF: u8 = 54;

pub fn main() {
    if let Err(err) = real_main() {
        eprintln!("gen_intrinsics: {err}");
        std::process::exit(1);
    }
}

fn real_main() -> GenResult<()> {
    let args = parse_args(env::args().skip(1))?;
    let generated = generate_with_inputs(&args.llvm_root)?;

    if args.build_script {
        emit_build_script_rerun_instructions(&args.llvm_root, &generated.inputs);
        write_generated_file(&args.generated_file, &generated.text)?;
        return Ok(());
    }

    if args.check {
        let existing = fs::read_to_string(&args.generated_file).map_err(|source| {
            GenError::new(format!(
                "failed to read {} for --check: {source}",
                args.generated_file.display()
            ))
        })?;
        if normalize_newlines(&existing) != normalize_newlines(&generated.text) {
            return Err(GenError::new(format!(
                "{} is stale; regenerate with gen_intrinsics {} {}",
                args.generated_file.display(),
                args.llvm_root.display(),
                args.generated_file.display()
            )));
        }
        return Ok(());
    }

    write_generated_file(&args.generated_file, &generated.text)?;
    Ok(())
}
#[derive(Debug)]
struct GeneratorArgs {
    check: bool,
    build_script: bool,
    llvm_root: PathBuf,
    generated_file: PathBuf,
}

struct GeneratedIntrinsics {
    text: String,
    inputs: Vec<PathBuf>,
}

fn parse_args<I>(args: I) -> GenResult<GeneratorArgs>
where
    I: IntoIterator<Item = String>,
{
    parse_args_with_out_dir(args, env::var_os("OUT_DIR").map(PathBuf::from))
}

fn parse_args_with_out_dir<I>(args: I, out_dir: Option<PathBuf>) -> GenResult<GeneratorArgs>
where
    I: IntoIterator<Item = String>,
{
    let mut args = args.into_iter().collect::<Vec<_>>();
    let check = if args.first().is_some_and(|arg| arg == "--check") {
        args.remove(0);
        true
    } else {
        false
    };

    let crate_root = Path::new(env!("CARGO_MANIFEST_DIR"));
    let build_script = !check && args.is_empty() && out_dir.is_some();
    let (llvm_root, generated_file) = match (args.as_slice(), out_dir.as_ref()) {
        ([], Some(out_dir)) if build_script => (
            crate_root.join(VENDORED_LLVM_ROOT),
            out_dir.join(GENERATED_INTRINSICS),
        ),
        ([generated_file], _) => (
            crate_root.join(VENDORED_LLVM_ROOT),
            PathBuf::from(generated_file),
        ),
        ([llvm_root, generated_file], _) => {
            (PathBuf::from(llvm_root), PathBuf::from(generated_file))
        }
        _ => {
            return Err(GenError::new(
                "usage: gen_intrinsics [--check] <generated-file>\n       gen_intrinsics [--check] <llvm-root> <generated-file>",
            ));
        }
    };

    Ok(GeneratorArgs {
        check,
        build_script,
        llvm_root,
        generated_file,
    })
}

fn normalize_newlines(s: &str) -> String {
    s.replace("\r\n", "\n")
}

fn generate_with_inputs(llvm_root: &Path) -> GenResult<GeneratedIntrinsics> {
    let mut engine = Engine::new(llvm_root.to_path_buf());
    engine.load_include(ROOT_TD)?;
    let text = engine.finish()?;
    let mut inputs = engine
        .loaded
        .iter()
        .map(|include| engine.resolve_include(include))
        .collect::<Vec<_>>();
    inputs.sort();
    Ok(GeneratedIntrinsics { text, inputs })
}

fn emit_build_script_rerun_instructions(llvm_root: &Path, inputs: &[PathBuf]) {
    println!("cargo:rerun-if-changed=build.rs");
    println!("cargo:rerun-if-changed=tools/gen_intrinsics.rs");
    for input in inputs {
        println!("cargo:rerun-if-changed={}", input.display());
    }
    println!("cargo:rerun-if-changed={}", llvm_root.display());
}

fn write_generated_file(path: &Path, content: &str) -> GenResult<()> {
    if let Ok(existing) = fs::read_to_string(path)
        && normalize_newlines(&existing) == normalize_newlines(content)
    {
        return Ok(());
    }

    if let Some(parent) = creatable_parent(path) {
        fs::create_dir_all(parent).map_err(|source| {
            GenError::new(format!("failed to create {}: {source}", parent.display()))
        })?;
    }
    fs::write(path, content)
        .map_err(|source| GenError::new(format!("failed to write {}: {source}", path.display())))
}

fn creatable_parent(path: &Path) -> Option<&Path> {
    path.parent()
        .filter(|parent| !parent.as_os_str().is_empty())
}

type GenResult<T> = Result<T, GenError>;

#[derive(Debug, Clone)]
struct GenError {
    message: String,
}

impl GenError {
    fn new(message: impl Into<String>) -> Self {
        Self {
            message: message.into(),
        }
    }
}

impl fmt::Display for GenError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        f.write_str(&self.message)
    }
}

impl std::error::Error for GenError {}

#[derive(Debug, Clone, PartialEq, Eq)]
enum TokenKind {
    Ident(String),
    BangIdent(String),
    String(String),
    Int(i64),
    LBrace,
    RBrace,
    LParen,
    RParen,
    LBracket,
    RBracket,
    Less,
    Greater,
    Colon,
    Semi,
    Comma,
    Equal,
    Dot,
    Ellipsis,
    Hash,
    Question,
}

#[derive(Debug, Clone)]
struct Token {
    kind: TokenKind,
    file: String,
    line: usize,
    col: usize,
}

struct Lexer<'a> {
    file: &'a str,
    text: &'a str,
    pos: usize,
    line: usize,
    col: usize,
}

impl<'a> Lexer<'a> {
    fn new(file: &'a str, text: &'a str) -> Self {
        Self {
            file,
            text,
            pos: 0,
            line: 1,
            col: 1,
        }
    }

    fn tokenize(mut self) -> GenResult<Vec<Token>> {
        let mut out = Vec::new();
        while let Some(ch) = self.peek_char() {
            if ch.is_whitespace() {
                self.bump_char();
                continue;
            }
            if ch == '/' && self.peek_next_char() == Some('/') {
                self.bump_char();
                self.bump_char();
                while let Some(c) = self.peek_char() {
                    self.bump_char();
                    if c == '\n' {
                        break;
                    }
                }
                continue;
            }
            if ch == '/' && self.peek_next_char() == Some('*') {
                self.bump_char();
                self.bump_char();
                loop {
                    match self.peek_char() {
                        Some('*') if self.peek_next_char() == Some('/') => {
                            self.bump_char();
                            self.bump_char();
                            break;
                        }
                        Some(_) => {
                            self.bump_char();
                        }
                        None => return Err(self.error("unterminated block comment")),
                    }
                }
                continue;
            }
            if ch == '#' && self.is_preprocessor_directive() && self.is_preprocessor_keyword() {
                while let Some(c) = self.peek_char() {
                    self.bump_char();
                    if c == '\n' {
                        break;
                    }
                }
                continue;
            }

            let file = self.file.to_owned();
            let line = self.line;
            let col = self.col;
            let kind = match ch {
                '{' => {
                    self.bump_char();
                    TokenKind::LBrace
                }
                '}' => {
                    self.bump_char();
                    TokenKind::RBrace
                }
                '(' => {
                    self.bump_char();
                    TokenKind::LParen
                }
                ')' => {
                    self.bump_char();
                    TokenKind::RParen
                }
                '[' => {
                    self.bump_char();
                    TokenKind::LBracket
                }
                ']' => {
                    self.bump_char();
                    TokenKind::RBracket
                }
                '<' => {
                    self.bump_char();
                    TokenKind::Less
                }
                '>' => {
                    self.bump_char();
                    TokenKind::Greater
                }
                ':' => {
                    self.bump_char();
                    TokenKind::Colon
                }
                ';' => {
                    self.bump_char();
                    TokenKind::Semi
                }
                ',' => {
                    self.bump_char();
                    TokenKind::Comma
                }
                '=' => {
                    self.bump_char();
                    TokenKind::Equal
                }
                '?' => {
                    self.bump_char();
                    TokenKind::Question
                }
                '#' => {
                    self.bump_char();
                    TokenKind::Hash
                }
                '.' => {
                    if self.starts_with("...") {
                        self.bump_char();
                        self.bump_char();
                        self.bump_char();
                        TokenKind::Ellipsis
                    } else {
                        self.bump_char();
                        TokenKind::Dot
                    }
                }
                '"' => TokenKind::String(self.lex_string()?),
                '!' => {
                    self.bump_char();
                    let ident = self.lex_ident_tail()?;
                    TokenKind::BangIdent(ident)
                }
                '-' | '0'..='9' => TokenKind::Int(self.lex_int()?),
                _ if is_ident_start(ch) => TokenKind::Ident(self.lex_ident()),
                _ => return Err(self.error(format!("unexpected character `{ch}`"))),
            };
            out.push(Token {
                kind,
                file,
                line,
                col,
            });
        }
        Ok(out)
    }

    fn is_preprocessor_directive(&self) -> bool {
        let before = &self.text[..self.pos];
        before
            .rsplit_once('\n')
            .map_or(before, |(_, tail)| tail)
            .trim()
            .is_empty()
    }

    fn is_preprocessor_keyword(&self) -> bool {
        self.starts_with("#if")
            || self.starts_with("#ifdef")
            || self.starts_with("#ifndef")
            || self.starts_with("#endif")
            || self.starts_with("#define")
            || self.starts_with("#undef")
    }

    fn lex_string(&mut self) -> GenResult<String> {
        self.expect_char('"')?;
        let mut s = String::new();
        while let Some(ch) = self.peek_char() {
            self.bump_char();
            match ch {
                '"' => return Ok(s),
                '\\' => {
                    let escaped = self
                        .peek_char()
                        .ok_or_else(|| self.error("unterminated string escape"))?;
                    self.bump_char();
                    let decoded = match escaped {
                        'n' => '\n',
                        'r' => '\r',
                        't' => '\t',
                        '\\' => '\\',
                        '"' => '"',
                        other => other,
                    };
                    s.push(decoded);
                }
                other => s.push(other),
            }
        }
        Err(self.error("unterminated string literal"))
    }

    fn lex_int(&mut self) -> GenResult<i64> {
        let start = self.pos;
        if self.peek_char() == Some('-') {
            self.bump_char();
        }
        if self.starts_with("0x") || self.starts_with("0X") {
            self.bump_char();
            self.bump_char();
            while self.peek_char().is_some_and(|c| c.is_ascii_hexdigit()) {
                self.bump_char();
            }
        } else {
            while self.peek_char().is_some_and(|c| c.is_ascii_digit()) {
                self.bump_char();
            }
        }
        let raw = &self.text[start..self.pos];
        let negative = raw.starts_with('-');
        let digits = raw.trim_start_matches('-');
        let parsed = if let Some(hex) = digits
            .strip_prefix("0x")
            .or_else(|| digits.strip_prefix("0X"))
        {
            i64::from_str_radix(hex, 16)
        } else {
            digits.parse::<i64>()
        };
        parsed
            .map(|v| if negative { -v } else { v })
            .map_err(|source| self.error(format!("invalid integer `{raw}`: {source}")))
    }

    fn lex_ident_tail(&mut self) -> GenResult<String> {
        if !self.peek_char().is_some_and(is_ident_start) {
            return Err(self.error("expected identifier after `!`"));
        }
        Ok(self.lex_ident())
    }

    fn lex_ident(&mut self) -> String {
        let start = self.pos;
        self.bump_char();
        while self.peek_char().is_some_and(is_ident_continue) {
            self.bump_char();
        }
        self.text[start..self.pos].to_owned()
    }

    fn expect_char(&mut self, expected: char) -> GenResult<()> {
        match self.peek_char() {
            Some(ch) if ch == expected => {
                self.bump_char();
                Ok(())
            }
            _ => Err(self.error(format!("expected `{expected}`"))),
        }
    }

    fn starts_with(&self, s: &str) -> bool {
        self.text[self.pos..].starts_with(s)
    }

    fn peek_char(&self) -> Option<char> {
        self.text[self.pos..].chars().next()
    }

    fn peek_next_char(&self) -> Option<char> {
        let mut chars = self.text[self.pos..].chars();
        chars.next()?;
        chars.next()
    }

    fn bump_char(&mut self) -> Option<char> {
        let ch = self.peek_char()?;
        self.pos += ch.len_utf8();
        if ch == '\n' {
            self.line += 1;
            self.col = 1;
        } else {
            self.col += 1;
        }
        Some(ch)
    }

    fn error(&self, message: impl Into<String>) -> GenError {
        GenError::new(format!(
            "{}:{}:{}: {}",
            self.file,
            self.line,
            self.col,
            message.into()
        ))
    }
}

fn is_ident_start(ch: char) -> bool {
    ch == '_' || ch.is_ascii_alphabetic()
}

fn is_ident_continue(ch: char) -> bool {
    ch == '_' || ch.is_ascii_alphanumeric()
}

#[derive(Debug, Clone)]
struct Located<T> {
    node: T,
    file: String,
    line: usize,
    col: usize,
}

impl<T> Located<T> {
    fn new(node: T, token: &Token) -> Self {
        Self {
            node,
            file: token.file.clone(),
            line: token.line,
            col: token.col,
        }
    }

    fn error(&self, message: impl Into<String>) -> GenError {
        GenError::new(format!(
            "{}:{}:{}: {}",
            self.file,
            self.line,
            self.col,
            message.into()
        ))
    }
}

#[derive(Debug, Clone)]
enum Stmt {
    Include(String),
    Class(TemplateDef),
    MultiClass(TemplateDef),
    Def(DefStmt),
    Defm(DefmStmt),
    Defvar(String, Expr),
    Defset(String, String, Vec<Located<Stmt>>),
    LetBlock(Vec<(String, Expr)>, Vec<Located<Stmt>>),
    Foreach(String, Expr, Vec<Located<Stmt>>),
    If(Expr, Vec<Located<Stmt>>, Vec<Located<Stmt>>),
    Field(String, Expr),
    Assert(Expr, Expr),
}

#[derive(Debug, Clone)]
struct TemplateDef {
    name: String,
    params: Vec<TemplateParam>,
    parents: Vec<Apply>,
    body: Vec<Located<Stmt>>,
}

#[derive(Debug, Clone)]
struct TemplateParam {
    name: String,
    default: Option<Expr>,
}

#[derive(Debug, Clone)]
struct DefStmt {
    name: Expr,
    parents: Vec<Apply>,
    body: Vec<Located<Stmt>>,
}

#[derive(Debug, Clone)]
struct DefmStmt {
    name: Expr,
    parents: Vec<Apply>,
}

#[derive(Debug, Clone)]
struct Apply {
    name: String,
    args: Vec<TemplateArg>,
}

#[derive(Debug, Clone)]
enum TemplateArg {
    Pos(Expr),
    Named(String, Expr),
}

#[derive(Debug, Clone)]
enum Expr {
    Ident(String),
    String(String),
    Int(i64),
    Bool(bool),
    Unset,
    List(Vec<Expr>),
    Apply(Apply),
    Field(Box<Expr>, String),
    Index(Box<Expr>, Box<Expr>),
    Concat(Box<Expr>, Box<Expr>),
    RangeInclusive(Box<Expr>, Box<Expr>),
    Bang(BangExpr),
}

impl Expr {
    fn contains_ident(&self, needle: &str) -> bool {
        match self {
            Expr::Ident(name) => name == needle,
            Expr::String(_) | Expr::Int(_) | Expr::Bool(_) | Expr::Unset => false,
            Expr::List(items) => items.iter().any(|item| item.contains_ident(needle)),
            Expr::Apply(app) => app.args.iter().any(|arg| match arg {
                TemplateArg::Pos(expr) | TemplateArg::Named(_, expr) => expr.contains_ident(needle),
            }),
            Expr::Field(base, _) => base.contains_ident(needle),
            Expr::Index(base, idx) | Expr::Concat(base, idx) | Expr::RangeInclusive(base, idx) => {
                base.contains_ident(needle) || idx.contains_ident(needle)
            }
            Expr::Bang(bang) => bang.contains_ident(needle),
        }
    }
}

#[derive(Debug, Clone)]
enum BangExpr {
    Call {
        op: String,
        type_arg: Option<String>,
        args: Vec<Expr>,
    },
    Cond(Vec<(Expr, Expr)>),
    Foreach(String, Box<Expr>, Box<Expr>),
    Filter(String, Box<Expr>, Box<Expr>),
    Foldl(Box<Expr>, Box<Expr>, String, String, Box<Expr>),
}

impl BangExpr {
    fn contains_ident(&self, needle: &str) -> bool {
        match self {
            BangExpr::Call { args, .. } => args.iter().any(|arg| arg.contains_ident(needle)),
            BangExpr::Cond(pairs) => pairs
                .iter()
                .any(|(cond, value)| cond.contains_ident(needle) || value.contains_ident(needle)),
            BangExpr::Foreach(var, list, body) | BangExpr::Filter(var, list, body) => {
                var == needle || list.contains_ident(needle) || body.contains_ident(needle)
            }
            BangExpr::Foldl(init, list, acc, var, body) => {
                acc == needle
                    || var == needle
                    || init.contains_ident(needle)
                    || list.contains_ident(needle)
                    || body.contains_ident(needle)
            }
        }
    }
}

struct Parser {
    tokens: Vec<Token>,
    pos: usize,
}

impl Parser {
    fn new(tokens: Vec<Token>) -> Self {
        Self { tokens, pos: 0 }
    }

    fn parse(mut self) -> GenResult<Vec<Located<Stmt>>> {
        let mut stmts = Vec::new();
        while !self.is_eof() {
            if self.eat(TokenKind::RBrace) {
                continue;
            }
            stmts.push(self.parse_stmt()?);
        }
        Ok(stmts)
    }

    fn parse_stmt(&mut self) -> GenResult<Located<Stmt>> {
        let token = self
            .peek()
            .ok_or_else(|| GenError::new("unexpected EOF"))?
            .clone();
        let stmt = if self.eat_ident("include") {
            let path = self.expect_string()?;
            Stmt::Include(path)
        } else if self.eat_ident("class") {
            Stmt::Class(self.parse_template_def()?)
        } else if self.eat_ident("multiclass") {
            Stmt::MultiClass(self.parse_template_def()?)
        } else if self.eat_ident("defm") {
            Stmt::Defm(self.parse_defm_after_keyword()?)
        } else if self.eat_ident("defvar") {
            let name = self.expect_ident()?;
            self.expect(TokenKind::Equal)?;
            let expr = self.parse_expr()?;
            self.expect(TokenKind::Semi)?;
            Stmt::Defvar(name, expr)
        } else if self.eat_ident("defset") {
            self.parse_defset_after_keyword()?
        } else if self.eat_ident("def") {
            Stmt::Def(self.parse_def_after_keyword()?)
        } else if self.eat_ident("foreach") {
            self.parse_foreach_after_keyword()?
        } else if self.eat_ident("let") {
            self.parse_let_after_keyword()?
        } else if self.eat_ident("if") {
            self.parse_if_after_keyword()?
        } else if self.eat_ident("assert") {
            let cond = self.parse_expr()?;
            self.expect(TokenKind::Comma)?;
            let msg = self.parse_expr()?;
            self.expect(TokenKind::Semi)?;
            Stmt::Assert(cond, msg)
        } else {
            self.parse_field_stmt()?
        };
        Ok(Located::new(stmt, &token))
    }

    fn parse_template_def(&mut self) -> GenResult<TemplateDef> {
        let name = self.expect_ident()?;
        let params = if self.eat(TokenKind::Less) {
            self.parse_template_params()?
        } else {
            Vec::new()
        };
        let parents = if self.eat(TokenKind::Colon) {
            self.parse_apply_list_until_body()?
        } else {
            Vec::new()
        };
        let body = if self.eat(TokenKind::LBrace) {
            self.parse_block_body()?
        } else {
            self.expect(TokenKind::Semi)?;
            Vec::new()
        };
        Ok(TemplateDef {
            name,
            params,
            parents,
            body,
        })
    }

    fn parse_template_params(&mut self) -> GenResult<Vec<TemplateParam>> {
        let mut params = Vec::new();
        if self.eat(TokenKind::Greater) {
            return Ok(params);
        }
        loop {
            let name = self.parse_typed_name_before(&[
                TokenKind::Equal,
                TokenKind::Comma,
                TokenKind::Greater,
            ])?;
            let default = if self.eat(TokenKind::Equal) {
                Some(self.parse_expr()?)
            } else {
                None
            };
            params.push(TemplateParam { name, default });
            if self.eat(TokenKind::Comma) {
                continue;
            }
            self.expect(TokenKind::Greater)?;
            break;
        }
        Ok(params)
    }

    fn parse_apply_list_until_body(&mut self) -> GenResult<Vec<Apply>> {
        let mut parents = Vec::new();
        loop {
            parents.push(self.parse_apply()?);
            if self.eat(TokenKind::Comma) {
                continue;
            }
            break;
        }
        Ok(parents)
    }

    fn parse_def_after_keyword(&mut self) -> GenResult<DefStmt> {
        let name = self.parse_expr()?;
        let parents = if self.eat(TokenKind::Colon) {
            self.parse_apply_list_until_body()?
        } else {
            Vec::new()
        };
        let body = if self.eat(TokenKind::LBrace) {
            self.parse_block_body()?
        } else {
            self.expect(TokenKind::Semi)?;
            Vec::new()
        };
        Ok(DefStmt {
            name,
            parents,
            body,
        })
    }

    fn parse_defm_after_keyword(&mut self) -> GenResult<DefmStmt> {
        let name = self.parse_expr()?;
        self.expect(TokenKind::Colon)?;
        let parents = self.parse_apply_list_until_body()?;
        self.expect(TokenKind::Semi)?;
        Ok(DefmStmt { name, parents })
    }

    fn parse_defset_after_keyword(&mut self) -> GenResult<Stmt> {
        let type_name = self.parse_defset_type_name()?;
        let name = self.expect_ident()?;
        self.expect(TokenKind::Equal)?;
        self.expect(TokenKind::LBrace)?;
        let body = self.parse_block_body()?;
        Ok(Stmt::Defset(name, type_name, body))
    }

    fn parse_defset_type_name(&mut self) -> GenResult<String> {
        if self.eat_ident("list") {
            self.expect(TokenKind::Less)?;
            let name = self.expect_ident()?;
            self.skip_balanced_type_tail()?;
            Ok(name)
        } else {
            self.expect_ident()
        }
    }

    fn skip_balanced_type_tail(&mut self) -> GenResult<()> {
        let mut depth = 1usize;
        while depth > 0 {
            let token = self
                .next()
                .ok_or_else(|| self.error_here("unexpected EOF in type"))?;
            match token.kind.clone() {
                TokenKind::Less => depth += 1,
                TokenKind::Greater => depth -= 1,
                _ => {}
            }
        }
        Ok(())
    }

    fn parse_foreach_after_keyword(&mut self) -> GenResult<Stmt> {
        let var = self.expect_ident()?;
        self.expect(TokenKind::Equal)?;
        let mut expr = self.parse_expr()?;
        if self.eat(TokenKind::Ellipsis) {
            let end = self.parse_expr()?;
            expr = Expr::RangeInclusive(Box::new(expr), Box::new(end));
        }
        self.expect_ident_value("in")?;
        let body = self.parse_stmt_or_block()?;
        Ok(Stmt::Foreach(var, expr, body))
    }

    fn parse_let_after_keyword(&mut self) -> GenResult<Stmt> {
        let assigns = self.parse_let_assignments()?;
        if self.eat_ident("in") {
            let body = self.parse_stmt_or_block()?;
            Ok(Stmt::LetBlock(assigns, body))
        } else if self.eat(TokenKind::Semi) {
            if assigns.len() != 1 {
                return Err(self.error_here("field let must contain exactly one assignment"));
            }
            let (name, expr) = assigns.into_iter().next().unwrap();
            Ok(Stmt::Field(name, expr))
        } else {
            let body = self.parse_stmt_or_block()?;
            Ok(Stmt::LetBlock(assigns, body))
        }
    }

    fn parse_let_assignments(&mut self) -> GenResult<Vec<(String, Expr)>> {
        let mut assigns = Vec::new();
        loop {
            let name = self.expect_ident()?;
            self.expect(TokenKind::Equal)?;
            let expr = self.parse_expr()?;
            assigns.push((name, expr));
            if self.eat(TokenKind::Comma) {
                continue;
            }
            break;
        }
        Ok(assigns)
    }

    fn parse_if_after_keyword(&mut self) -> GenResult<Stmt> {
        let cond = self.parse_expr()?;
        self.expect_ident_value("then")?;
        let then_body = self.parse_stmt_or_block()?;
        let else_body = if self.eat_ident("else") {
            self.parse_stmt_or_block()?
        } else {
            Vec::new()
        };
        Ok(Stmt::If(cond, then_body, else_body))
    }

    fn parse_field_stmt(&mut self) -> GenResult<Stmt> {
        let name = self.parse_typed_name_before(&[TokenKind::Equal])?;
        self.expect(TokenKind::Equal)?;
        let expr = self.parse_expr()?;
        if !self.eat(TokenKind::Semi)
            && !matches!(
                self.peek().map(|tok| &tok.kind),
                Some(TokenKind::RBrace | TokenKind::Ident(_))
            )
        {
            return Err(self.error_here("expected `;`"));
        }
        Ok(Stmt::Field(name, expr))
    }

    fn parse_typed_name_before(&mut self, stops: &[TokenKind]) -> GenResult<String> {
        let start = self.pos;
        let mut depth_angle = 0usize;
        let mut candidate = None;
        while let Some(token) = self.peek() {
            let at_stop =
                depth_angle == 0 && stops.iter().any(|stop| token.kind.same_variant(stop));
            if at_stop {
                break;
            }
            let token = self.next().unwrap();
            match token.kind.clone() {
                TokenKind::Less => depth_angle += 1,
                TokenKind::Greater => depth_angle = depth_angle.saturating_sub(1),
                TokenKind::Ident(name) if depth_angle == 0 => candidate = Some(name),
                _ => {}
            }
        }
        candidate.ok_or_else(|| {
            self.pos = start;
            self.error_here("expected typed name")
        })
    }

    fn parse_stmt_or_block(&mut self) -> GenResult<Vec<Located<Stmt>>> {
        if self.eat(TokenKind::LBrace) {
            self.parse_block_body()
        } else {
            Ok(vec![self.parse_stmt()?])
        }
    }

    fn parse_block_body(&mut self) -> GenResult<Vec<Located<Stmt>>> {
        let mut body = Vec::new();
        while !self.eat(TokenKind::RBrace) {
            if self.is_eof() {
                return Err(self.error_here("unexpected EOF in block"));
            }
            body.push(self.parse_stmt()?);
        }
        Ok(body)
    }

    fn parse_apply(&mut self) -> GenResult<Apply> {
        let name = self.expect_ident()?;
        let args = if self.eat(TokenKind::Less) {
            self.parse_template_args()?
        } else {
            Vec::new()
        };
        Ok(Apply { name, args })
    }

    fn parse_template_args(&mut self) -> GenResult<Vec<TemplateArg>> {
        let mut args = Vec::new();
        if self.eat(TokenKind::Greater) {
            return Ok(args);
        }
        loop {
            if let Some((name, expr)) = self.try_parse_named_arg()? {
                args.push(TemplateArg::Named(name, expr));
            } else {
                args.push(TemplateArg::Pos(self.parse_expr()?));
            }
            if self.eat(TokenKind::Comma) {
                continue;
            }
            self.expect(TokenKind::Greater)?;
            break;
        }
        Ok(args)
    }

    fn try_parse_named_arg(&mut self) -> GenResult<Option<(String, Expr)>> {
        let save = self.pos;
        if let Some(Token {
            kind: TokenKind::Ident(name),
            ..
        }) = self.peek().cloned()
        {
            self.next();
            if self.eat(TokenKind::Equal) {
                let expr = self.parse_expr()?;
                return Ok(Some((name, expr)));
            }
        }
        self.pos = save;
        Ok(None)
    }

    fn parse_expr(&mut self) -> GenResult<Expr> {
        self.parse_concat()
    }

    fn parse_concat(&mut self) -> GenResult<Expr> {
        let mut expr = self.parse_postfix()?;
        while self.eat(TokenKind::Hash) {
            let rhs = self.parse_postfix()?;
            expr = Expr::Concat(Box::new(expr), Box::new(rhs));
        }
        Ok(expr)
    }

    fn parse_postfix(&mut self) -> GenResult<Expr> {
        let mut expr = self.parse_primary()?;
        loop {
            if self.eat(TokenKind::Dot) {
                let field = self.expect_ident()?;
                expr = Expr::Field(Box::new(expr), field);
            } else if self.eat(TokenKind::LBracket) {
                let index = self.parse_expr()?;
                self.expect(TokenKind::RBracket)?;
                expr = Expr::Index(Box::new(expr), Box::new(index));
            } else {
                break;
            }
        }
        Ok(expr)
    }

    fn parse_primary(&mut self) -> GenResult<Expr> {
        let token = self
            .next()
            .ok_or_else(|| GenError::new("unexpected EOF in expression"))?;
        match token.kind.clone() {
            TokenKind::Ident(name) => {
                if name == "true" {
                    Ok(Expr::Bool(true))
                } else if name == "false" {
                    Ok(Expr::Bool(false))
                } else if self.eat(TokenKind::Less) {
                    let args = self.parse_template_args_after_less()?;
                    Ok(Expr::Apply(Apply { name, args }))
                } else {
                    Ok(Expr::Ident(name))
                }
            }
            TokenKind::String(s) => Ok(Expr::String(s)),
            TokenKind::Int(i) => Ok(Expr::Int(i)),
            TokenKind::Question => Ok(Expr::Unset),
            TokenKind::LBracket => self.parse_list_after_lbracket(),
            TokenKind::LParen => {
                let expr = self.parse_expr()?;
                self.expect(TokenKind::RParen)?;
                Ok(expr)
            }
            TokenKind::BangIdent(op) => self.parse_bang(op),
            other => Err(Located::new(other, &token).error("expected expression")),
        }
    }

    fn parse_template_args_after_less(&mut self) -> GenResult<Vec<TemplateArg>> {
        self.parse_template_args()
    }

    fn parse_list_after_lbracket(&mut self) -> GenResult<Expr> {
        let mut items = Vec::new();
        if self.eat(TokenKind::RBracket) {
            self.eat_type_suffix()?;
            return Ok(Expr::List(items));
        }
        loop {
            items.push(self.parse_expr()?);
            if self.eat(TokenKind::Comma) {
                if self.eat(TokenKind::RBracket) {
                    self.eat_type_suffix()?;
                    break;
                }
                continue;
            }
            self.expect(TokenKind::RBracket)?;
            self.eat_type_suffix()?;
            break;
        }
        Ok(Expr::List(items))
    }

    fn eat_type_suffix(&mut self) -> GenResult<()> {
        if self.eat(TokenKind::Less) {
            let mut depth = 1usize;
            while depth > 0 {
                let token = self
                    .next()
                    .ok_or_else(|| self.error_here("unexpected EOF in list type suffix"))?;
                match token.kind.clone() {
                    TokenKind::Less => depth += 1,
                    TokenKind::Greater => depth -= 1,
                    _ => {}
                }
            }
        }
        Ok(())
    }

    fn parse_bang(&mut self, op: String) -> GenResult<Expr> {
        let type_arg = if matches!(op.as_str(), "cast" | "isa") && self.eat(TokenKind::Less) {
            let ty = self.expect_ident()?;
            self.expect(TokenKind::Greater)?;
            Some(ty)
        } else {
            None
        };
        self.expect(TokenKind::LParen)?;
        let bang = match op.as_str() {
            "cond" => {
                let mut pairs = Vec::new();
                loop {
                    let cond = self.parse_expr()?;
                    self.expect(TokenKind::Colon)?;
                    let value = self.parse_expr()?;
                    pairs.push((cond, value));
                    if self.eat(TokenKind::Comma) {
                        if self.eat(TokenKind::RParen) {
                            break;
                        }
                        continue;
                    }
                    self.expect(TokenKind::RParen)?;
                    break;
                }
                BangExpr::Cond(pairs)
            }
            "foreach" | "filter" => {
                let var = self.expect_ident()?;
                self.expect(TokenKind::Comma)?;
                let list = self.parse_expr()?;
                self.expect(TokenKind::Comma)?;
                let body = self.parse_expr()?;
                self.expect(TokenKind::RParen)?;
                if op == "foreach" {
                    BangExpr::Foreach(var, Box::new(list), Box::new(body))
                } else {
                    BangExpr::Filter(var, Box::new(list), Box::new(body))
                }
            }
            "foldl" => {
                let init = self.parse_expr()?;
                self.expect(TokenKind::Comma)?;
                let list = self.parse_expr()?;
                self.expect(TokenKind::Comma)?;
                let acc = self.expect_ident()?;
                self.expect(TokenKind::Comma)?;
                let var = self.expect_ident()?;
                self.expect(TokenKind::Comma)?;
                let body = self.parse_expr()?;
                self.expect(TokenKind::RParen)?;
                BangExpr::Foldl(Box::new(init), Box::new(list), acc, var, Box::new(body))
            }
            _ => {
                let mut args = Vec::new();
                if self.eat(TokenKind::RParen) {
                    BangExpr::Call { op, type_arg, args }
                } else {
                    loop {
                        args.push(self.parse_expr()?);
                        if self.eat(TokenKind::Comma) {
                            continue;
                        }
                        self.expect(TokenKind::RParen)?;
                        break;
                    }
                    BangExpr::Call { op, type_arg, args }
                }
            }
        };
        Ok(Expr::Bang(bang))
    }

    fn expect_string(&mut self) -> GenResult<String> {
        let token = self
            .next()
            .ok_or_else(|| self.error_here("expected string"))?;
        match token.kind.clone() {
            TokenKind::String(s) => Ok(s),
            other => Err(Located::new(other, &token).error("expected string")),
        }
    }

    fn expect_ident(&mut self) -> GenResult<String> {
        let token = self
            .next()
            .ok_or_else(|| self.error_here("expected identifier"))?;
        match token.kind.clone() {
            TokenKind::Ident(s) => Ok(s),
            other => Err(Located::new(other, &token).error("expected identifier")),
        }
    }

    fn expect_ident_value(&mut self, expected: &str) -> GenResult<()> {
        let got = self.expect_ident()?;
        if got == expected {
            Ok(())
        } else {
            Err(self.error_here(format!("expected `{expected}`, got `{got}`")))
        }
    }

    fn eat_ident(&mut self, expected: &str) -> bool {
        if matches!(self.peek().map(|t| &t.kind), Some(TokenKind::Ident(s)) if s == expected) {
            self.pos += 1;
            true
        } else {
            false
        }
    }

    fn eat(&mut self, kind: TokenKind) -> bool {
        if self.peek().is_some_and(|tok| tok.kind.same_variant(&kind)) {
            self.pos += 1;
            true
        } else {
            false
        }
    }

    fn expect(&mut self, kind: TokenKind) -> GenResult<()> {
        let token = self
            .next()
            .ok_or_else(|| self.error_here("unexpected EOF"))?;
        if token.kind.same_variant(&kind) {
            Ok(())
        } else {
            Err(Located::new(token.kind.clone(), &token)
                .error(format!("expected `{}`", kind.display())))
        }
    }

    fn next(&mut self) -> Option<Token> {
        let token = self.tokens.get(self.pos).cloned()?;
        self.pos += 1;
        Some(token)
    }

    fn peek(&self) -> Option<&Token> {
        self.tokens.get(self.pos)
    }

    fn is_eof(&self) -> bool {
        self.pos >= self.tokens.len()
    }

    fn error_here(&self, message: impl Into<String>) -> GenError {
        if let Some(tok) = self.peek() {
            GenError::new(format!(
                "{}:{}:{}: {}",
                tok.file,
                tok.line,
                tok.col,
                message.into()
            ))
        } else {
            GenError::new(message.into())
        }
    }
}

impl TokenKind {
    fn same_variant(&self, other: &TokenKind) -> bool {
        std::mem::discriminant(self) == std::mem::discriminant(other)
    }

    fn display(&self) -> &'static str {
        match self {
            TokenKind::Ident(_) => "identifier",
            TokenKind::BangIdent(_) => "bang identifier",
            TokenKind::String(_) => "string",
            TokenKind::Int(_) => "integer",
            TokenKind::LBrace => "{",
            TokenKind::RBrace => "}",
            TokenKind::LParen => "(",
            TokenKind::RParen => ")",
            TokenKind::LBracket => "[",
            TokenKind::RBracket => "]",
            TokenKind::Less => "<",
            TokenKind::Greater => ">",
            TokenKind::Colon => ":",
            TokenKind::Semi => ";",
            TokenKind::Comma => ",",
            TokenKind::Equal => "=",
            TokenKind::Dot => ".",
            TokenKind::Ellipsis => "...",
            TokenKind::Hash => "#",
            TokenKind::Question => "?",
        }
    }
}

#[derive(Debug, Clone)]
enum Value {
    Unset,
    Int(i64),
    Bool(bool),
    String(String),
    List(Vec<Value>),
    Record(Rc<RecordValue>),
    SelfRef(String, String),
}

#[derive(Debug, Clone)]
struct RecordValue {
    name: Option<String>,
    classes: BTreeSet<String>,
    fields: BTreeMap<String, Value>,
    id: usize,
}

#[derive(Debug, Default)]
struct RecordBuilder {
    name: Option<String>,
    classes: BTreeSet<String>,
    fields: BTreeMap<String, Value>,
}

impl RecordBuilder {
    fn merge(&mut self, other: Rc<RecordValue>) {
        self.classes.extend(other.classes.iter().cloned());
        for (name, value) in &other.fields {
            self.fields.insert(name.clone(), value.clone());
        }
    }

    fn finish(self, id: usize) -> Rc<RecordValue> {
        Rc::new(RecordValue {
            name: self.name,
            classes: self.classes,
            fields: self.fields,
            id,
        })
    }
}

#[derive(Debug, Clone, Default)]
struct Scope {
    values: HashMap<String, Value>,
}

#[derive(Debug, Clone, Default)]
struct EnvStack {
    scopes: Vec<Scope>,
}

impl EnvStack {
    fn new() -> Self {
        Self {
            scopes: vec![Scope::default()],
        }
    }

    fn push(&mut self) {
        self.scopes.push(Scope::default());
    }

    fn pop(&mut self) {
        self.scopes.pop();
    }

    fn set(&mut self, name: impl Into<String>, value: Value) {
        self.scopes
            .last_mut()
            .expect("scope stack has root")
            .values
            .insert(name.into(), value);
    }

    fn get(&self, name: &str) -> Option<Value> {
        self.scopes
            .iter()
            .rev()
            .find_map(|scope| scope.values.get(name).cloned())
    }
}

#[derive(Debug, Clone)]
struct LetOverride {
    name: String,
    value: Expr,
}

struct Engine {
    llvm_root: PathBuf,
    loaded: HashSet<String>,
    classes: HashMap<String, TemplateDef>,
    multiclasses: HashMap<String, TemplateDef>,
    records: BTreeMap<String, Rc<RecordValue>>,
    record_order: Vec<Rc<RecordValue>>,
    globals: EnvStack,
    let_stack: Vec<LetOverride>,
    next_record_id: Cell<usize>,
}

impl Engine {
    fn new(llvm_root: PathBuf) -> Self {
        Self {
            llvm_root,
            loaded: HashSet::new(),
            classes: HashMap::new(),
            multiclasses: HashMap::new(),
            records: BTreeMap::new(),
            record_order: Vec::new(),
            globals: EnvStack::new(),
            let_stack: Vec::new(),
            next_record_id: Cell::new(1),
        }
    }

    fn load_include(&mut self, include: &str) -> GenResult<()> {
        let mut env = self.globals.clone();
        self.load_include_into(include, &mut env)?;
        self.globals = env;
        Ok(())
    }

    fn load_include_into(&mut self, include: &str, env: &mut EnvStack) -> GenResult<()> {
        if !self.loaded.insert(include.to_owned()) {
            return Ok(());
        }
        let path = self.resolve_include(include);
        let text = fs::read_to_string(&path).map_err(|source| {
            GenError::new(format!("failed to read {}: {source}", path.display()))
        })?;
        let tokens = Lexer::new(include, &text).tokenize()?;
        let stmts = Parser::new(tokens).parse()?;
        for stmt in &stmts {
            self.execute_stmt(stmt, env, None)?;
        }
        Ok(())
    }

    fn resolve_include(&self, include: &str) -> PathBuf {
        let trimmed = include.strip_prefix("llvm/").unwrap_or(include);
        self.llvm_root.join("include").join("llvm").join(trimmed)
    }

    fn append_active_let_fields(&self, def: &mut TemplateDef, loc: &Located<Stmt>) {
        for let_override in &self.let_stack {
            def.body.push(Located {
                node: Stmt::Field(let_override.name.clone(), let_override.value.clone()),
                file: loc.file.clone(),
                line: loc.line,
                col: loc.col,
            });
        }
    }

    fn eval_assert(
        &mut self,
        cond: &Expr,
        msg: &Expr,
        env: &mut EnvStack,
        loc: &Located<Stmt>,
    ) -> GenResult<()> {
        if assert_references_skipped_field(cond) || assert_references_skipped_field(msg) {
            return Ok(());
        }
        if self.eval_bool(cond, env)? {
            Ok(())
        } else {
            let message = self.eval_to_string(msg, env)?;
            Err(loc.error(format!("assertion failed: {message}")))
        }
    }

    fn execute_stmt(
        &mut self,
        stmt: &Located<Stmt>,
        env: &mut EnvStack,
        multi_prefix: Option<&str>,
    ) -> GenResult<()> {
        match &stmt.node {
            Stmt::Include(path) => self.load_include_into(path, env),
            Stmt::Class(def) => {
                let mut def = def.clone();
                self.append_active_let_fields(&mut def, stmt);
                self.classes.insert(def.name.clone(), def);
                Ok(())
            }
            Stmt::MultiClass(def) => {
                let mut def = def.clone();
                self.append_active_let_fields(&mut def, stmt);
                self.multiclasses.insert(def.name.clone(), def);
                Ok(())
            }
            Stmt::Def(def) => self.execute_def(stmt, def, env, multi_prefix).map(|_| ()),
            Stmt::Defm(defm) => self.execute_defm(stmt, defm, env, multi_prefix),
            Stmt::Defvar(name, expr) => {
                let value = self.eval(expr, env)?;
                env.set(name.clone(), value);
                Ok(())
            }
            Stmt::Defset(name, type_name, body) => {
                let before = self.record_order.len();
                for child in body {
                    self.execute_stmt(child, env, multi_prefix)?;
                }
                let list = self.record_order[before..]
                    .iter()
                    .filter(|record| record.classes.contains(type_name))
                    .cloned()
                    .map(Value::Record)
                    .collect::<Vec<_>>();
                env.set(name.clone(), Value::List(list));
                Ok(())
            }
            Stmt::LetBlock(assigns, body) => {
                let old_len = self.let_stack.len();
                self.let_stack
                    .extend(assigns.iter().map(|(name, value)| LetOverride {
                        name: name.clone(),
                        value: value.clone(),
                    }));
                for child in body {
                    self.execute_stmt(child, env, multi_prefix)?;
                }
                self.let_stack.truncate(old_len);
                Ok(())
            }
            Stmt::Foreach(var, expr, body) => {
                let values = self.eval_to_list(expr, env)?;
                for value in values {
                    env.push();
                    env.set(var.clone(), value);
                    for child in body {
                        self.execute_stmt(child, env, multi_prefix)?;
                    }
                    env.pop();
                }
                Ok(())
            }
            Stmt::If(cond, then_body, else_body) => {
                let body = if self.eval_bool(cond, env)? {
                    then_body
                } else {
                    else_body
                };
                for child in body {
                    self.execute_stmt(child, env, multi_prefix)?;
                }
                Ok(())
            }
            Stmt::Field(_, _) => Ok(()),
            Stmt::Assert(cond, msg) => self.eval_assert(cond, msg, env, stmt),
        }
    }

    fn execute_def(
        &mut self,
        stmt: &Located<Stmt>,
        def: &DefStmt,
        env: &mut EnvStack,
        multi_prefix: Option<&str>,
    ) -> GenResult<Rc<RecordValue>> {
        let raw_name = self.eval_name(&def.name, env)?;
        let name = if let Some(prefix) = multi_prefix {
            if def.name.contains_ident("NAME") {
                raw_name
            } else {
                format!("{prefix}{raw_name}")
            }
        } else {
            raw_name
        };

        if self.records.contains_key(&name) {
            return Err(stmt.error(format!("duplicate def `{name}`")));
        }

        env.push();
        env.set("NAME", Value::String(name.clone()));
        let mut builder = RecordBuilder {
            name: Some(name.clone()),
            ..RecordBuilder::default()
        };
        for parent in &def.parents {
            let record = self.instantiate_class(parent, Some(&name), env)?;
            builder.merge(record);
        }
        self.apply_body_fields(&def.body, env, &mut builder)?;
        self.apply_let_overrides(env, &mut builder)?;
        env.pop();

        let record = builder.finish(self.alloc_record_id());
        self.records.insert(name.clone(), record.clone());
        self.record_order.push(record.clone());
        env.set(name, Value::Record(record.clone()));
        Ok(record)
    }

    fn execute_defm(
        &mut self,
        stmt: &Located<Stmt>,
        defm: &DefmStmt,
        env: &mut EnvStack,
        multi_prefix: Option<&str>,
    ) -> GenResult<()> {
        let raw_name = self.eval_name(&defm.name, env)?;
        let prefix = if let Some(parent_prefix) = multi_prefix {
            if defm.name.contains_ident("NAME") {
                raw_name
            } else {
                format!("{parent_prefix}{raw_name}")
            }
        } else {
            raw_name
        };
        for parent in &defm.parents {
            if self.multiclasses.contains_key(&parent.name) {
                self.expand_multiclass(stmt, parent, &prefix, env)?;
            } else if self.classes.contains_key(&parent.name) {
                // Defm class parents in this intrinsic closure are marker classes
                // used by upstream backends; they do not affect the IR intrinsic
                // metadata emitted here.
                continue;
            } else {
                return Err(stmt.error(format!("unknown multiclass `{}`", parent.name)));
            }
        }
        Ok(())
    }

    fn expand_multiclass(
        &mut self,
        stmt: &Located<Stmt>,
        app: &Apply,
        prefix: &str,
        env: &mut EnvStack,
    ) -> GenResult<()> {
        let def = self
            .multiclasses
            .get(&app.name)
            .cloned()
            .ok_or_else(|| stmt.error(format!("unknown multiclass `{}`", app.name)))?;
        env.push();
        env.set("NAME", Value::String(prefix.to_owned()));
        self.bind_template_args(&def.params, &app.args, env, Some(prefix))?;
        for parent in &def.parents {
            self.expand_multiclass(stmt, parent, prefix, env)?;
        }
        for child in &def.body {
            self.execute_stmt(child, env, Some(prefix))?;
        }
        env.pop();
        Ok(())
    }

    fn instantiate_class(
        &mut self,
        app: &Apply,
        record_name: Option<&str>,
        env: &mut EnvStack,
    ) -> GenResult<Rc<RecordValue>> {
        let def = self
            .classes
            .get(&app.name)
            .cloned()
            .ok_or_else(|| GenError::new(format!("unknown class `{}`", app.name)))?;
        env.push();
        let name_value = record_name.unwrap_or(&app.name).to_owned();
        env.set("NAME", Value::String(name_value.clone()));
        self.bind_template_args(&def.params, &app.args, env, record_name)?;
        let mut builder = RecordBuilder::default();
        builder.classes.insert(def.name.clone());
        for param in &def.params {
            if let Some(value) = env.get(&param.name) {
                builder.fields.insert(param.name.clone(), value);
            }
        }
        for parent in &def.parents {
            let record = self.instantiate_class(parent, record_name, env)?;
            builder.merge(record);
        }
        for (field, value) in &builder.fields {
            env.set(field.clone(), value.clone());
        }
        self.apply_body_fields(&def.body, env, &mut builder)?;
        env.pop();
        Ok(builder.finish(0))
    }

    fn bind_template_args(
        &mut self,
        params: &[TemplateParam],
        args: &[TemplateArg],
        env: &mut EnvStack,
        record_name: Option<&str>,
    ) -> GenResult<()> {
        let mut positional = Vec::new();
        let mut named = HashMap::new();
        for arg in args {
            match arg {
                TemplateArg::Pos(expr) => positional.push(expr),
                TemplateArg::Named(name, expr) => {
                    named.insert(name.as_str(), expr);
                }
            }
        }
        for (idx, param) in params.iter().enumerate() {
            let value = if let Some(expr) = positional.get(idx) {
                self.eval(expr, env)?
            } else if let Some(expr) = named.get(param.name.as_str()) {
                self.eval(expr, env)?
            } else if let Some(default) = &param.default {
                self.eval(default, env)?
            } else {
                Value::Unset
            };
            env.set(param.name.clone(), value);
        }
        if let Some(name) = record_name {
            env.set("NAME", Value::String(name.to_owned()));
        }
        Ok(())
    }

    fn apply_body_fields(
        &mut self,
        body: &[Located<Stmt>],
        env: &mut EnvStack,
        builder: &mut RecordBuilder,
    ) -> GenResult<()> {
        for stmt in body {
            match &stmt.node {
                Stmt::Field(name, expr) => {
                    if name == "TypeInfo" {
                        let value = self.synthesize_type_info(env)?;
                        builder.fields.insert(name.clone(), value.clone());
                        env.set(name.clone(), value);
                        continue;
                    }
                    if should_skip_field_eval(name) {
                        builder.fields.insert(name.clone(), Value::Unset);
                        env.set(name.clone(), Value::Unset);
                        continue;
                    }
                    let value = self.eval(expr, env).map_err(|err| {
                        GenError::new(format!(
                            "{} while evaluating field `{}` on `{}`",
                            err.message,
                            name,
                            builder.name.as_deref().unwrap_or("<anonymous>")
                        ))
                    })?;
                    builder.fields.insert(name.clone(), value.clone());
                    env.set(name.clone(), value);
                }
                Stmt::Assert(cond, msg) => self.eval_assert(cond, msg, env, stmt)?,
                other => {
                    self.execute_stmt(
                        &Located {
                            node: other.clone(),
                            file: stmt.file.clone(),
                            line: stmt.line,
                            col: stmt.col,
                        },
                        env,
                        None,
                    )?;
                }
            }
        }
        Ok(())
    }

    fn apply_let_overrides(
        &mut self,
        env: &mut EnvStack,
        builder: &mut RecordBuilder,
    ) -> GenResult<()> {
        for let_override in self.let_stack.clone() {
            let value = self.eval(&let_override.value, env)?;
            builder
                .fields
                .insert(let_override.name.clone(), value.clone());
            env.set(let_override.name, value);
        }
        Ok(())
    }

    fn synthesize_type_info(&self, env: &mut EnvStack) -> GenResult<Value> {
        let ret_types = match env.get("RetTypes").ok_or_else(|| {
            GenError::new("TypeInfo synthesis requires RetTypes to be evaluated first")
        })? {
            Value::List(items) => items,
            other => {
                return Err(GenError::new(format!(
                    "TypeInfo synthesis expected RetTypes list, got {other:?}"
                )));
            }
        };
        let param_types = match env.get("ParamTypes").ok_or_else(|| {
            GenError::new("TypeInfo synthesis requires ParamTypes to be evaluated first")
        })? {
            Value::List(items) => items,
            other => {
                return Err(GenError::new(format!(
                    "TypeInfo synthesis expected ParamTypes list, got {other:?}"
                )));
            }
        };
        let all_values = ret_types
            .iter()
            .cloned()
            .chain(param_types.iter().cloned())
            .collect::<Vec<_>>();
        let all_types = all_values
            .iter()
            .map(type_from_value)
            .collect::<GenResult<Vec<_>>>()?;
        let is_overloaded = all_types.iter().any(IntrType::is_any);
        let type_sig = compute_type_signature(&ret_types, &param_types)?;

        let mut builder = RecordBuilder::default();
        builder.classes.insert("TypeInfoGen".to_owned());
        builder
            .fields
            .insert("RetTypes".to_owned(), Value::List(ret_types));
        builder
            .fields
            .insert("ParamTypes".to_owned(), Value::List(param_types));
        builder
            .fields
            .insert("AllTypes".to_owned(), Value::List(all_values.clone()));
        builder
            .fields
            .insert("Types".to_owned(), Value::List(all_values));
        builder
            .fields
            .insert("isOverloaded".to_owned(), Value::Bool(is_overloaded));
        builder.fields.insert(
            "TypeSig".to_owned(),
            Value::List(
                type_sig
                    .into_iter()
                    .map(|value| Value::Int(value as i64))
                    .collect(),
            ),
        );
        Ok(Value::Record(builder.finish(self.alloc_record_id())))
    }

    fn eval_synthetic_field(
        &mut self,
        base: &Expr,
        field: &str,
        env: &mut EnvStack,
    ) -> GenResult<Option<Value>> {
        let Expr::Apply(app) = base else {
            return Ok(None);
        };
        if app.name != "ResolveArgCode" || field != "ret" {
            return Ok(None);
        }
        let args = positional_args(app)?;
        if args.len() != 4 {
            return Err(GenError::new("ResolveArgCode requires four arguments"));
        }

        let ax = self.eval_int(args[3], env).map_err(|err| {
            GenError::new(format!(
                "{} while resolving ResolveArgCode encoded operand",
                err.message
            ))
        })?;
        let ah = ax & 0xFF00;
        let al = ax & 0x00FF;
        let ret = match ah {
            0x100 => {
                let ac_idx = self.eval_int(args[2], env).map_err(|err| {
                    GenError::new(format!(
                        "{} while resolving EncAnyType ACIdx for ax {ax:#x}",
                        err.message
                    ))
                })?;
                (ac_idx << 3) | al
            }
            0x200 => {
                let mapping = self.eval_int_list(args[0], env)?;
                let num = list_int_at(&mapping, al, "ResolveArgCode Mapping")?;
                (num << 3) | 7
            }
            0x300 => {
                let mapping = self.eval_int_list(args[0], env).map_err(|err| {
                    GenError::new(format!(
                        "{} while evaluating ResolveArgCode Mapping",
                        err.message
                    ))
                })?;
                let arg_codes = self.eval_int_list(args[1], env).map_err(|err| {
                    GenError::new(format!(
                        "{} while evaluating ResolveArgCode ArgCodes",
                        err.message
                    ))
                })?;
                let num = list_int_at(&mapping, al, "ResolveArgCode Mapping")?;
                (num << 3) | list_int_at(&arg_codes, num, "ResolveArgCode ArgCodes")?
            }
            0x400 => self.eval_int(args[2], env).map_err(|err| {
                GenError::new(format!(
                    "{} while resolving EncNextArgA ACIdx for ax {ax:#x}",
                    err.message
                ))
            })?,
            0x500 => {
                let mapping = self.eval_int_list(args[0], env)?;
                list_int_at(&mapping, al, "ResolveArgCode Mapping")?
            }
            _ => al,
        };
        Ok(Some(Value::Int(ret)))
    }

    fn eval_int_list(&mut self, expr: &Expr, env: &mut EnvStack) -> GenResult<Vec<i64>> {
        let mut out = Vec::new();
        for value in self.eval_to_list(expr, env)? {
            out.push(self.value_to_int(value)?);
        }
        Ok(out)
    }

    fn eval(&mut self, expr: &Expr, env: &mut EnvStack) -> GenResult<Value> {
        match expr {
            Expr::Ident(name) => {
                if let Some(value) = env.get(name) {
                    Ok(value)
                } else if let Some(record) = self.records.get(name) {
                    Ok(Value::Record(record.clone()))
                } else {
                    Ok(Value::String(name.clone()))
                }
            }
            Expr::String(s) => Ok(Value::String(s.clone())),
            Expr::Int(i) => Ok(Value::Int(*i)),
            Expr::Bool(b) => Ok(Value::Bool(*b)),
            Expr::Unset => Ok(Value::Unset),
            Expr::List(items) => items
                .iter()
                .map(|item| self.eval(item, env))
                .collect::<GenResult<Vec<_>>>()
                .map(Value::List),
            Expr::Apply(app) => self.instantiate_class(app, None, env).map(Value::Record),
            Expr::Field(base, field) => {
                if let Some(value) = self.eval_synthetic_field(base, field, env)? {
                    return Ok(value);
                }
                let value = self.eval(base, env)?;
                self.field_value(value, field)
            }
            Expr::Index(base, idx) => {
                let value = self.eval(base, env)?;
                let idx = self.eval_int(idx, env)?;
                self.index_value(value, idx)
            }
            Expr::Concat(lhs, rhs) => {
                let lhs = self.eval(lhs, env)?;
                let rhs = self.eval(rhs, env)?;
                match (lhs, rhs) {
                    (Value::List(mut lhs), Value::List(rhs)) => {
                        lhs.extend(rhs);
                        Ok(Value::List(lhs))
                    }
                    (lhs, rhs) => Ok(Value::String(format!(
                        "{}{}",
                        self.value_to_string(lhs)?,
                        self.value_to_string(rhs)?
                    ))),
                }
            }
            Expr::RangeInclusive(start, end) => {
                let start = self.eval_int(start, env)?;
                let end = self.eval_int(end, env)?;
                Ok(Value::List((start..=end).map(Value::Int).collect()))
            }
            Expr::Bang(bang) => self.eval_bang(bang, env),
        }
    }

    fn eval_bang(&mut self, bang: &BangExpr, env: &mut EnvStack) -> GenResult<Value> {
        match bang {
            BangExpr::Call { op, type_arg, args } => {
                self.eval_bang_call(op, type_arg.as_deref(), args, env)
            }
            BangExpr::Cond(pairs) => {
                for (cond, value) in pairs {
                    if self.eval_bool(cond, env)? {
                        return self.eval(value, env);
                    }
                }
                Ok(Value::Unset)
            }
            BangExpr::Foreach(var, list, body) => {
                let values = self.eval_to_list(list, env)?;
                let mut out = Vec::with_capacity(values.len());
                for value in values {
                    env.push();
                    env.set(var.clone(), value);
                    out.push(self.eval(body, env)?);
                    env.pop();
                }
                Ok(Value::List(out))
            }
            BangExpr::Filter(var, list, pred) => {
                let values = self.eval_to_list(list, env)?;
                let mut out = Vec::new();
                for value in values {
                    env.push();
                    env.set(var.clone(), value.clone());
                    if self.eval_bool(pred, env)? {
                        out.push(value);
                    }
                    env.pop();
                }
                Ok(Value::List(out))
            }
            BangExpr::Foldl(init, list, acc, var, body) => {
                let mut accumulator = self.eval(init, env)?;
                let values = self.eval_to_list(list, env)?;
                for value in values {
                    env.push();
                    env.set(acc.clone(), accumulator);
                    env.set(var.clone(), value);
                    accumulator = self.eval(body, env)?;
                    env.pop();
                }
                Ok(accumulator)
            }
        }
    }

    fn eval_variadic_and(&mut self, args: &[Expr], env: &mut EnvStack) -> GenResult<Value> {
        let values = args
            .iter()
            .map(|arg| self.eval(arg, env))
            .collect::<GenResult<Vec<_>>>()?;
        if values.iter().all(|value| matches!(value, Value::Bool(_))) {
            return Ok(Value::Bool(
                values
                    .iter()
                    .all(|value| matches!(value, Value::Bool(true))),
            ));
        }
        let mut acc = !0_i64;
        for value in values {
            acc &= self.value_to_int(value)?;
        }
        Ok(Value::Int(acc))
    }

    fn eval_variadic_or(&mut self, args: &[Expr], env: &mut EnvStack) -> GenResult<Value> {
        let values = args
            .iter()
            .map(|arg| self.eval(arg, env))
            .collect::<GenResult<Vec<_>>>()?;
        if values.iter().all(|value| matches!(value, Value::Bool(_))) {
            return Ok(Value::Bool(
                values
                    .iter()
                    .any(|value| matches!(value, Value::Bool(true))),
            ));
        }
        let mut acc = 0_i64;
        for value in values {
            acc |= self.value_to_int(value)?;
        }
        Ok(Value::Int(acc))
    }

    fn value_to_int(&self, value: Value) -> GenResult<i64> {
        match value {
            Value::Int(i) => Ok(i),
            Value::Bool(b) => Ok(if b { 1 } else { 0 }),
            other => Err(GenError::new(format!("expected integer, got {other:?}"))),
        }
    }

    fn eval_bang_call(
        &mut self,
        op: &str,
        type_arg: Option<&str>,
        args: &[Expr],
        env: &mut EnvStack,
    ) -> GenResult<Value> {
        match op {
            "add" => Ok(Value::Int(
                self.eval_int(&args[0], env)? + self.eval_int(&args[1], env)?,
            )),
            "sub" => Ok(Value::Int(
                self.eval_int(&args[0], env)? - self.eval_int(&args[1], env)?,
            )),
            "mul" => Ok(Value::Int(
                self.eval_int(&args[0], env)? * self.eval_int(&args[1], env)?,
            )),
            "div" => Ok(Value::Int(
                self.eval_int(&args[0], env)? / self.eval_int(&args[1], env)?,
            )),
            "and" => self.eval_variadic_and(args, env),
            "or" => self.eval_variadic_or(args, env),
            "xor" => Ok(Value::Int(
                self.eval_int(&args[0], env)? ^ self.eval_int(&args[1], env)?,
            )),
            "shl" => Ok(Value::Int(
                self.eval_int(&args[0], env)? << self.eval_int(&args[1], env)?,
            )),
            "not" => Ok(Value::Bool(!self.eval_bool(&args[0], env)?)),
            "eq" => {
                let lhs = self.eval(&args[0], env)?;
                let rhs = self.eval(&args[1], env)?;
                Ok(Value::Bool(self.value_eq(lhs, rhs)))
            }
            "ne" => {
                let lhs = self.eval(&args[0], env)?;
                let rhs = self.eval(&args[1], env)?;
                Ok(Value::Bool(!self.value_eq(lhs, rhs)))
            }
            "le" => Ok(Value::Bool(
                self.eval_int(&args[0], env)? <= self.eval_int(&args[1], env)?,
            )),
            "lt" => Ok(Value::Bool(
                self.eval_int(&args[0], env)? < self.eval_int(&args[1], env)?,
            )),
            "ge" => Ok(Value::Bool(
                self.eval_int(&args[0], env)? >= self.eval_int(&args[1], env)?,
            )),
            "gt" => Ok(Value::Bool(
                self.eval_int(&args[0], env)? > self.eval_int(&args[1], env)?,
            )),
            "if" => {
                if self.eval_bool(&args[0], env)? {
                    self.eval(&args[1], env)
                } else {
                    self.eval(&args[2], env)
                }
            }
            "listconcat" => {
                let mut out = Vec::new();
                for arg in args {
                    out.extend(self.eval_to_list(arg, env)?);
                }
                Ok(Value::List(out))
            }
            "listsplat" => {
                let value = self.eval(&args[0], env)?;
                let count = self.eval_int(&args[1], env)?;
                Ok(Value::List((0..count).map(|_| value.clone()).collect()))
            }
            "listflatten" => {
                let mut out = Vec::new();
                for item in self.eval_to_list(&args[0], env)? {
                    match item {
                        Value::List(items) => out.extend(items),
                        other => out.push(other),
                    }
                }
                Ok(Value::List(out))
            }
            "substr" => {
                let source = self.eval_to_string(&args[0], env)?;
                let start = self.eval_int(&args[1], env)? as usize;
                let len = if args.len() > 2 {
                    Some(self.eval_int(&args[2], env)? as usize)
                } else {
                    None
                };
                let chars = source.chars().skip(start);
                let result: String = if let Some(len) = len {
                    chars.take(len).collect()
                } else {
                    chars.collect()
                };
                Ok(Value::String(result))
            }
            "size" => match self.eval(&args[0], env)? {
                Value::List(items) => Ok(Value::Int(items.len() as i64)),
                Value::String(s) => Ok(Value::Int(s.len() as i64)),
                other => Err(GenError::new(format!("!size unsupported for {other:?}"))),
            },
            "empty" => match self.eval(&args[0], env)? {
                Value::List(items) => Ok(Value::Bool(items.is_empty())),
                Value::String(s) => Ok(Value::Bool(s.is_empty())),
                Value::Unset => Ok(Value::Bool(true)),
                other => Err(GenError::new(format!("!empty unsupported for {other:?}"))),
            },
            "range" => {
                if args.len() == 1 {
                    match self.eval(&args[0], env)? {
                        Value::List(items) => Ok(Value::List(
                            (0..items.len() as i64).map(Value::Int).collect(),
                        )),
                        Value::Int(end) => Ok(Value::List((0..end).map(Value::Int).collect())),
                        other => Err(GenError::new(format!("!range unsupported for {other:?}"))),
                    }
                } else {
                    let start = self.eval_int(&args[0], env)?;
                    let end = self.eval_int(&args[1], env)?;
                    Ok(Value::List((start..end).map(Value::Int).collect()))
                }
            }
            "tail" => {
                let mut values = self.eval_to_list(&args[0], env)?;
                if !values.is_empty() {
                    values.remove(0);
                }
                Ok(Value::List(values))
            }
            "head" => {
                let values = self.eval_to_list(&args[0], env)?;
                Ok(values.into_iter().next().unwrap_or(Value::Unset))
            }
            "strconcat" => {
                let mut out = String::new();
                for arg in args {
                    out.push_str(&self.eval_to_string(arg, env)?);
                }
                Ok(Value::String(out))
            }
            "subst" => {
                let needle = self.eval_to_string(&args[0], env)?;
                let replacement = self.eval_to_string(&args[1], env)?;
                let haystack = self.eval_to_string(&args[2], env)?;
                Ok(Value::String(haystack.replace(&needle, &replacement)))
            }
            "find" => {
                let haystack = self.eval_to_string(&args[0], env)?;
                let needle = self.eval_to_string(&args[1], env)?;
                Ok(Value::Int(
                    haystack.find(&needle).map_or(-1, |idx| idx as i64),
                ))
            }
            "isa" => {
                let ty = type_arg.ok_or_else(|| GenError::new("!isa requires a type argument"))?;
                let value = self.eval(&args[0], env)?;
                Ok(Value::Bool(self.value_is_a(&value, ty)))
            }
            "cast" => {
                let ty = type_arg.ok_or_else(|| GenError::new("!cast requires a type argument"))?;
                let value = self.eval(&args[0], env)?;
                self.cast_value(value, ty)
            }
            other => Err(GenError::new(format!(
                "unsupported bang operator `!{other}`"
            ))),
        }
    }

    fn field_value(&self, value: Value, field: &str) -> GenResult<Value> {
        match value {
            Value::Record(record) => record.fields.get(field).cloned().ok_or_else(|| {
                GenError::new(format!("record {:?} has no field `{field}`", record.name))
            }),
            Value::SelfRef(name, _) if field == "NAME" => Ok(Value::String(name)),
            Value::String(s) if field == "NAME" => Ok(Value::String(s)),
            other => Err(GenError::new(format!(
                "cannot read field `{field}` from {other:?}"
            ))),
        }
    }

    fn index_value(&self, value: Value, index: i64) -> GenResult<Value> {
        let index = usize::try_from(index).map_err(|_| GenError::new("negative list index"))?;
        match value {
            Value::List(items) => items
                .get(index)
                .cloned()
                .ok_or_else(|| GenError::new(format!("list index {index} out of bounds"))),
            Value::String(s) => s
                .chars()
                .nth(index)
                .map(|ch| Value::String(ch.to_string()))
                .ok_or_else(|| GenError::new(format!("string index {index} out of bounds"))),
            other => Err(GenError::new(format!("cannot index {other:?}"))),
        }
    }

    fn cast_value(&self, value: Value, ty: &str) -> GenResult<Value> {
        match value {
            Value::Record(record) => {
                if record.classes.contains(ty) || record.name.as_deref() == Some(ty) {
                    Ok(Value::Record(record))
                } else {
                    Err(GenError::new(format!(
                        "record {:?} is not a `{ty}`",
                        record.name
                    )))
                }
            }
            Value::String(name) => {
                if let Some(record) = self.records.get(&name) {
                    if record.classes.contains(ty) || record.name.as_deref() == Some(ty) {
                        Ok(Value::Record(record.clone()))
                    } else {
                        Err(GenError::new(format!("record `{name}` is not a `{ty}`")))
                    }
                } else {
                    Ok(Value::SelfRef(name, ty.to_owned()))
                }
            }
            other => Err(GenError::new(format!("cannot !cast {other:?} to `{ty}`"))),
        }
    }

    fn value_is_a(&self, value: &Value, ty: &str) -> bool {
        match value {
            Value::Record(record) => {
                record.classes.contains(ty) || record.name.as_deref() == Some(ty)
            }
            Value::SelfRef(_, class) => class == ty,
            _ => false,
        }
    }

    fn eval_bool(&mut self, expr: &Expr, env: &mut EnvStack) -> GenResult<bool> {
        match self.eval(expr, env)? {
            Value::Bool(b) => Ok(b),
            Value::Int(i) => Ok(i != 0),
            Value::String(s) => Ok(!s.is_empty()),
            Value::List(items) => Ok(!items.is_empty()),
            Value::Unset => Ok(false),
            Value::Record(_) | Value::SelfRef(_, _) => Ok(true),
        }
    }

    fn eval_int(&mut self, expr: &Expr, env: &mut EnvStack) -> GenResult<i64> {
        match self.eval(expr, env)? {
            Value::Int(i) => Ok(i),
            Value::Bool(b) => Ok(if b { 1 } else { 0 }),
            Value::String(s) => s
                .parse::<i64>()
                .map_err(|source| GenError::new(format!("expected integer, got `{s}`: {source}"))),
            other => Err(GenError::new(format!("expected integer, got {other:?}"))),
        }
    }

    fn eval_to_string(&mut self, expr: &Expr, env: &mut EnvStack) -> GenResult<String> {
        let value = match expr {
            Expr::Ident(name) if env.get(name).is_none() && !self.records.contains_key(name) => {
                Value::String(name.clone())
            }
            _ => self.eval(expr, env)?,
        };
        self.value_to_string(value)
    }

    fn eval_name(&mut self, expr: &Expr, env: &mut EnvStack) -> GenResult<String> {
        self.eval_to_string(expr, env)
    }

    fn value_to_string(&self, value: Value) -> GenResult<String> {
        match value {
            Value::String(s) => Ok(s),
            Value::Int(i) => Ok(i.to_string()),
            Value::Bool(b) => Ok(if b { "1" } else { "0" }.to_owned()),
            Value::Record(record) => record
                .name
                .clone()
                .ok_or_else(|| GenError::new("anonymous record cannot be converted to string")),
            Value::SelfRef(name, _) => Ok(name),
            Value::Unset => Ok(String::new()),
            Value::List(_) => Err(GenError::new("list cannot be converted to string")),
        }
    }

    fn eval_to_list(&mut self, expr: &Expr, env: &mut EnvStack) -> GenResult<Vec<Value>> {
        match self.eval(expr, env)? {
            Value::List(values) => Ok(values),
            other => Err(GenError::new(format!("expected list, got {other:?}"))),
        }
    }

    fn value_eq(&self, lhs: Value, rhs: Value) -> bool {
        match (lhs, rhs) {
            (Value::Unset, Value::Unset) => true,
            (Value::Int(a), Value::Int(b)) => a == b,
            (Value::Bool(a), Value::Bool(b)) => a == b,
            (Value::String(a), Value::String(b)) => a == b,
            (Value::Int(a), Value::Bool(b)) | (Value::Bool(b), Value::Int(a)) => (a != 0) == b,
            (Value::Record(a), Value::Record(b)) => {
                if a.name.is_some() || b.name.is_some() {
                    a.name == b.name
                } else {
                    a.classes == b.classes && a.fields.len() == b.fields.len()
                }
            }
            (Value::Record(a), Value::String(b)) | (Value::String(b), Value::Record(a)) => {
                a.name.as_deref() == Some(b.as_str())
            }
            (Value::List(a), Value::List(b)) => {
                a.len() == b.len() && a.into_iter().zip(b).all(|(a, b)| self.value_eq(a, b))
            }
            _ => false,
        }
    }

    fn alloc_record_id(&self) -> usize {
        let id = self.next_record_id.get();
        self.next_record_id.set(id + 1);
        id
    }

    fn finish(&mut self) -> GenResult<String> {
        let mut intrinsics = self.collect_intrinsics()?;
        intrinsics.sort_by(|lhs, rhs| {
            (lhs.target_prefix.is_empty(), &lhs.name, lhs.record_id).cmp(&(
                rhs.target_prefix.is_empty(),
                &rhs.name,
                rhs.record_id,
            ))
        });
        // The tuple above sorted target intrinsics first because false < true; flip by stable key.
        intrinsics.sort_by(|lhs, rhs| {
            let lhs_has_target = !lhs.target_prefix.is_empty();
            let rhs_has_target = !rhs.target_prefix.is_empty();
            (lhs_has_target, &lhs.name, lhs.record_id).cmp(&(
                rhs_has_target,
                &rhs.name,
                rhs.record_id,
            ))
        });
        self.check_duplicates(&intrinsics)?;
        let target_sets = build_target_sets(&intrinsics);
        let required_targets = [
            "",
            "aarch64",
            "amdgcn",
            "arm",
            "bpf",
            "dx",
            "hexagon",
            "loongarch",
            "mips",
            "nvvm",
            "ppc",
            "r600",
            "riscv",
            "s390",
            "spv",
            "ve",
            "wasm",
            "x86",
            "xcore",
        ];
        let actual_targets = target_sets
            .iter()
            .map(|set| set.prefix.as_str())
            .collect::<Vec<_>>();
        if actual_targets != required_targets {
            return Err(GenError::new(format!(
                "target partition mismatch: expected {:?}, got {:?}",
                required_targets, actual_targets
            )));
        }
        render_generated(&intrinsics, &target_sets)
    }

    fn collect_intrinsics(&mut self) -> GenResult<Vec<IntrinsicOut>> {
        let default_properties = self
            .record_order
            .iter()
            .filter(|record| record.classes.contains("IntrinsicProperty"))
            .filter(|record| {
                field_bool(record, "IsDefault")
                    .unwrap_or(None)
                    .unwrap_or(false)
            })
            .cloned()
            .collect::<Vec<_>>();

        let mut out = Vec::new();
        for record in &self.record_order {
            if !record.classes.contains("Intrinsic") {
                continue;
            }
            let record_name = record
                .name
                .as_deref()
                .ok_or_else(|| GenError::new("anonymous intrinsic record"))?;
            if !record_name.starts_with("int_") {
                return Err(GenError::new(format!(
                    "intrinsic record `{record_name}` does not start with int_"
                )));
            }
            let enum_name = record_name.trim_start_matches("int_").to_owned();
            let target_prefix = field_string(record, "TargetPrefix")?.unwrap_or_default();
            let explicit_name = field_string(record, "LLVMName")?.unwrap_or_default();
            let default_name = format!("llvm.{}", enum_name.replace('_', "."));
            let name = if explicit_name.is_empty() {
                default_name
            } else {
                if !explicit_name.starts_with("llvm.") {
                    return Err(GenError::new(format!(
                        "intrinsic `{record_name}` explicit name `{explicit_name}` does not start with llvm."
                    )));
                }
                explicit_name
            };
            if !target_prefix.is_empty() {
                let expected = format!("llvm.{target_prefix}.");
                if !name.starts_with(&expected) {
                    return Err(GenError::new(format!(
                        "target intrinsic `{record_name}` name `{name}` does not start with `{expected}`"
                    )));
                }
            }
            let ret_types = field_list(record, "RetTypes")?;
            let param_types = field_list(record, "ParamTypes")?;
            let mut properties = field_list(record, "IntrProperties")?;
            if !field_bool(record, "DisableDefaultAttributes")?.unwrap_or(true) {
                properties.extend(default_properties.iter().cloned().map(Value::Record));
            }
            let attr_info = compute_attrs(&properties)?;
            let type_sig = compute_type_signature(&ret_types, &param_types)?;
            let samples = compute_sample_overloads(&ret_types, &param_types)?;
            let clang_builtin = field_string(record, "ClangBuiltinName")?.filter(|s| !s.is_empty());
            let ms_builtin = field_string(record, "MSBuiltinName")?.filter(|s| !s.is_empty());
            out.push(IntrinsicOut {
                enum_name,
                name,
                target_prefix,
                overloaded: samples.is_some(),
                type_sig,
                fn_attrs: attr_info.fn_attrs,
                arg_attrs: attr_info.arg_attrs,
                memory_effects: attr_info.memory_effects,
                clang_builtin,
                ms_builtin,
                pretty_print: attr_info.pretty_print,
                sample_overloads: samples.unwrap_or_default(),
                record_id: record.id,
            });
        }
        Ok(out)
    }

    fn check_duplicates(&self, intrinsics: &[IntrinsicOut]) -> GenResult<()> {
        for pair in intrinsics.windows(2) {
            if pair[0].name == pair[1].name {
                return Err(GenError::new(format!(
                    "duplicate intrinsic name `{}` from `{}` and `{}`",
                    pair[0].name, pair[0].enum_name, pair[1].enum_name
                )));
            }
        }
        Ok(())
    }
}

